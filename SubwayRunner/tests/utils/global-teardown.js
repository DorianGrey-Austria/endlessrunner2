// 🧹 GLOBAL TEARDOWN - Runs after all tests
import fs from 'fs';
import path from 'path';

async function globalTeardown(config) {
  console.log('🧹 Starting Global Test Teardown...');

  const reportsDir = path.join(process.cwd(), 'tests/reports');
  const sessionInfoPath = path.join(reportsDir, 'session-info.json');

  if (fs.existsSync(sessionInfoPath)) {
    const sessionInfo = JSON.parse(fs.readFileSync(sessionInfoPath, 'utf8'));

    // Update session with end time
    sessionInfo.endTime = new Date().toISOString();
    sessionInfo.duration = new Date(sessionInfo.endTime) - new Date(sessionInfo.startTime);

    fs.writeFileSync(sessionInfoPath, JSON.stringify(sessionInfo, null, 2));
  }

  // Generate test summary
  const summaryPath = path.join(reportsDir, 'test-summary.md');
  const summary = generateTestSummary();

  fs.writeFileSync(summaryPath, summary);

  console.log('✅ Global teardown completed');
  console.log(`📊 Test reports available in: ${reportsDir}`);
}

function generateTestSummary() {
  const timestamp = new Date().toISOString();

  return `# 🧪 Test Execution Summary

**Generated**: ${timestamp}

## Test Results
- See \`test-results.json\` for detailed results
- See \`html-report/\` for interactive report

## Performance Metrics
- See \`performance/\` directory for FPS and memory data

## Visual Comparisons
- See \`visual/screenshots/\` for visual regression tests

## Next Steps
1. Review failed tests in HTML report
2. Check performance metrics for regressions
3. Update visual baselines if UI changes are intentional
4. Investigate any cache-busting issues

---
*Generated by Playwright Test Suite*
`;
}

export default globalTeardown;